package manejodearchivo;

/**
 *
 * @author Stalin
 */
public class DatosRimpe {
    private String ruc;
    private String nombreContribuyente;
    private String zonal;
    private String regimen;
    private String negocioPopular;

    public DatosRimpe() {
    }

    public String getRuc() {
        return ruc;
    }

    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    public String getNombreContribuyente() {
        return nombreContribuyente;
    }

    public void setNombreContribuyente(String nombreContribuyente) {
        this.nombreContribuyente = nombreContribuyente;
    }

    public String getZonal() {
        return zonal;
    }

    public void setZonal(String zonal) {
        this.zonal = zonal;
    }

    public String getRegimen() {
        return regimen;
    }

    public void setRegimen(String regimen) {
        this.regimen = regimen;
    }

    public String getNegocioPopular() {
        return negocioPopular;
    }

    public void setNegocioPopular(String negocioPopular) {
        this.negocioPopular = negocioPopular;
    }
    
    
}
